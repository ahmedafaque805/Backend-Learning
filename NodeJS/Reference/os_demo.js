const os = require('os')

// Platform
console.log(os.platform())

// CPU arch
console.log(os.arch())

// CPU Core info
console.log(os.cpus())

// Free Memory
console.log(os.freemem())

// Total Memory
console.log(os.totalmem())

// Home Directory
console.log(os.homedir())

// UpTime
console.log(os.uptime())

// Hostname
console.log(os.hostname())

// User Info
console.log(os.userInfo())

// OS Version
console.log(os.version())
